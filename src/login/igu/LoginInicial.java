    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        
        String usuario = txtUsuario.getText();
        String contrasenia = txtContrasenia.getText();
        Usuario usr  = control.validarUsuario(usuario, contrasenia);
               
        
        
        
        if (usr != null) {
            String rol = usr.getUnRol().getNombreRol();// trae UnRol(que esta en la clase y trae el nombre de rol)
            
            if (rol.equals("admin")) {
                PrincipalAdmin pAdmin = new PrincipalAdmin(control, usr);// Si nombreRol es admin va para este lado
                pAdmin.setVisible(true);
                pAdmin.setLocationRelativeTo(null);
                this.dispose();
            }
            if (rol.equals("user")) {
                PrincipalUser pUser = new PrincipalUser(control, usr);// Si nombreRol es user va para este lado
                
                pUser.setVisible(true);
                pUser.setLocationRelativeTo(null);
                this.dispose();
            }
            
        }
        else {
            txtMensaje.setText("Usuario o contraseña incorrectos");// aca se coloca el mensaje que retorna desde la logica
        }
        
        
        
    }//GEN-LAST:event_btnLoginActionPerformed

    

